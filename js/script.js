// first pic fade in
function test1(){
  document.getElementById("pic1").setAttribute(
    "style", "opacity: 0.2; transition: .5s ease;"
  );
  document.getElementById("text1").setAttribute(
    "style", "font-size: 36px; display: block; text-align: center; color: white;"
  );
  //first pic fade out
}
function out1(){
  document.getElementById("pic1").setAttribute(
    "style", "opacity: 1.0; "
  );
  document.getElementById("text1").setAttribute(
    "style", "display: none; "
  );
}

